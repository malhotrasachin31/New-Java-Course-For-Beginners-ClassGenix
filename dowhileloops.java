
package loopsjava;
public class dowhileloops {
    public static void main(String args[]){
        
        System.out.println("All about while loops");
        
        int lap = 100;
        while(lap<10){// condition==false
            System.out.println("Keep racing..");
            lap++;
        }
        
        System.out.println("All about do while loops");
        
        do{
          System.out.println("Keep racing..");
          lap++;
        }while(lap<10);
                
        
        
        
    }
}
